# 2do CASO - este bucle se ejecuta una cantidad de veces ingresada x teclado
	
# hacer un programa que muestre un numero desde el 0 hasta un numero ingresado x teclado
cant=int(input("Ingrese la cantidad de veces a procesar el bucle: "))
num=0
while num<=cant: # se pueden poner multiples condiciones para evaluar
	print("El numero es (corresponde al while 2do caso): ",num)
	num=num+1 
# si anulo el cambio de valor de num, entonces entro en un ciclo infinito (loop) 








